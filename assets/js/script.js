$(function(){
  $('#panier').click(function(){
    $('#basket').show();
  });
  $('#close').click(function(){
    $('#basket').hide();
  });
  $('#closex').click(function(){
    $('#basket').hide();
  });
  $('#PCPortable').click(function(){
    $('.fixe').hide();
    $('.portable').show();
    $('.tab').hide();
  });
  $('#PCFixe').click(function(){
    $('.fixe').show();
    $('.portable').hide();
    $('.tab').hide();
  });
  $('#Tab').click(function(){
    $('.fixe').hide();
    $('.portable').hide();
    $('.tab').show();
  });

});
